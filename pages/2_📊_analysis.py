#         - 차량 (연료 구분)별로 어느 지역에서 많이 판매되었는지 확인 -> 해당 지역에서 전시장 및 지점 확장
#             - 전반적으로 어떤 연료 타입의 차량이 많이 판매되었는지 확인하고 추후 차량 개발 전략 수정 가능
#         - 지역별 차량 구매 유형 및 연령대 분석 -> 해당 지역의 고객 성향 파악 가능
#             - 1인당 GDP가 높거나 인구 밀도가 높은 지역에서 마케팅 실시 가능
#         - 구매 기준 시점 따른 연료 타입별 차량 구입 건수 확인 -> 어떤 차량의 판매가 추후 증가할 것인지 예측 가능
#             - 수소차 판매량 감소 양상 : 2024년 4분기부터 지속된 수소 폭발 사고 파장
#             - 전기차 판매량 증가 양상 : 2023년 2분기부터 전기차 보조금 확대
#             - 디젤차 판매량 증감 양상 : 2023년 3분기를 기점으로 디젤차 마지막 세대 출시 -> 일시적인 판매량 증가
#                 - 2023년 4분기부터 디젤차 환경 부담금 확대
#         - 고객 구분(개인, 법인)별 차량 판매 대수 확인
#             - 2023년 2분기 및 2024년 3분기에 판매량 급감하는 양상
#             - 비트코인 폭등으로 인해 제조업계 반도체 유통 급감 -> 반도체 수급 어려움 발생
#         - 연령대별 차량 구매 횟수 분석 : 어떤 연령층에서 차량 구매 수요가 높은가
#             - 20대 초반, 30대 중반, 50대 초반, 60대 후반에 증가하는 양상
#             - 고객들의 차량의 교체 주기가 약 15년임을 확인 가능
#                 - 해당 연령대의 고객의 선호 차량 유형을 파악하여 타겟팅 가능
#         - 연령대별 회원가입 고객 분석 : 어떤 연령대의 고객이 많은가?
#             - 20대 중반, 40대 초반, 50대 중반, 60대 중반이 많았음
#                 - 20대 중반 : 대학 졸업/취업 등과 맞물려 차량 구매에 대한 관심 증가
#                 - 40대 초반 : 미성년자 자녀를 둔 가정에서의 패밀리카 구매 관심도 증가
#                 - 50대 중반 : 자녀의 대학 졸업/취업 선물로 차량 구매 관심도 증가
#                 - 60대 중반 : 개인의 만족을 위한 차량 구매 관심도 증가
#             - 차량 구매 이후 회원 가입한 경우도 존재
#                 - 차량 구매 시 안내를 통해 회원가입, 이후 포인트 등 혜택 사용하기 위함
#                 - 차량 구매 시점 + 1~2세를 하면 현재 나이인 경우가 많음
#         - 성별에 따른 구매 차량 유형 분석
#         - 연도별 차종별 판매량 분석
#         - 분기 및 요일별 차량 판매량 분석
